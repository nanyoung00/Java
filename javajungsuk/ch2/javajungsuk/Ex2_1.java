package javajungsuk;

public class Ex2_1 {

	public static void main(String[] args) {
		
		System.out.println("Hello");
		System.out.println("Hello");
		System.out.println("Hello");
		// print() - 출력 후에 줄바꿈을 안함
		// println() - 출력 후에 줄바꿈을 한다
		
		System.out.println("3+5");
		// 이렇게 하면 문자로 출력돼서 3+5가 나온다
		System.out.println(3+5); // 덧셈
		System.out.println(3-5); // 뺄셈
		System.out.println(5*3); // 곱셈
		System.out.println(5/3); // 나눗셈
		System.out.println(5/3.0); // 이게 왜 이런지는 나중에 나옴

	}

}
